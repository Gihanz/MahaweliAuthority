    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        String TankName = tankName.getText();
        String TankEngineer = tankEngineer.getText();
        int TankMaxDepth = Integer.parseInt(tankMaxDepth.getText());
        String TankDetails = tankDetails.getText();
        
        TankModel model = new TankModel(TankName, TankEngineer, TankDetails, TankMaxDepth);

        try {
            String isAdd = TankController.addTank(model);
            if (isAdd.equals("true")) {
                JOptionPane.showMessageDialog(this,"Add successful");
            }else{
                JOptionPane.showMessageDialog(this,"Add Unsuccessful");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,"Add Unsuccessful");
        }
        clearData();
    }//GEN-LAST:event_SearchButtonActionPerformed

